package exception;

public class RemoveElementException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public RemoveElementException() {
	}

	public RemoveElementException(String arg0) {
		super(arg0);
	}

	public RemoveElementException(Throwable arg0) {
		super(arg0);
	}

	public RemoveElementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
